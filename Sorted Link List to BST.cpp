class Solution{
  public:
    TNode* helper(vector<int>& v,int s,int e)
    {
        if(s>e)return nullptr;
        int mid=(s+e+1)/2;
        TNode* temp=new TNode(v[mid]);
        temp->left=helper(v,s,mid-1);
        temp->right=helper(v,mid+1,e);
        
        return temp;
    }
    TNode* sortedListToBST(LNode *head) {
        vector<int> v;
        int count =0;
        
        while(head)
        {
            v.push_back(head->data);
            head=head->next;
            count++;
        }
        int start=0,end=count-1;
        
        return helper(v,start,end);
    }
};

